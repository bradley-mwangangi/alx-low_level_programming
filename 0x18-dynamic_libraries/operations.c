int add(int a, int b);
int sub(int a, int b);
int mul(int a, int b);
int div(int a, int b);
int mod(int a, int b);

/**
 * add - adds two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: returns result of (a + b)
*/
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: returns result of (a - b)
*/
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: returns result of (a * b)
*/
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: returns result of (a / b)
*/
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - evaluates the modulus of a % b
 * @a: first integer
 * @b: second integer
 *
 * Return: returns result of (a % b)
*/
int mod(int a, int b)
{
	return (a % b);
}
